DROP TABLE DOCTOR CASCADE;
DROP TABLE ESPECIALIDAD CASCADE;
DROP TABLE DOCTOR_ESPECIALIDAD CASCADE;
-- 
CREATE TABLE DOCTOR (
    ID SERIAL NOT NULL PRIMARY KEY,
    SEXO CHAR NOT NULL,
    NOMBRE VARCHAR(100) NOT NULL,
    APELLIDO VARCHAR(100) NOT NULL,
    FOTO TEXT NOT NULL
);
CREATE TABLE ESPECIALIDAD (
    ID SERIAL NOT NULL PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL
);
CREATE TABLE DOCTOR_ESPECIALIDAD(
    FK_DOCTOR INT NOT NULL ,
    FK_ESPECIALIDAD INT NOT NULL ,
    FOREIGN KEY (FK_DOCTOR) REFERENCES DOCTOR(ID),
    FOREIGN KEY (FK_ESPECIALIDAD) REFERENCES ESPECIALIDAD(ID),
    PRIMARY KEY (FK_DOCTOR,FK_ESPECIALIDAD)
);